export function getSymbolsData(): Symbol[] {
  return symbolsData;
}

export function updateSymbolsData(newSymbols: Symbol[]) {
  symbolsData = newSymbols;
}

export function getSymbols() {
  return { symbols: symbolsData };
}

export function uploadSymbolsFile(file: File) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = (e) => {
      try {
        const text = e.target?.result as string;
        const newSymbols = JSON.parse(text);
        if (Array.isArray(newSymbols)) {
          updateSymbolsData([...symbolsData, ...newSymbols]);
          resolve({ success: true, message: 'Symbols uploaded successfully' });
        } else {
          reject(new Error('Invalid file format'));
        }
      } catch (error) {
        reject(error);
      }
    };
    reader.onerror = () => reject(new Error('Failed to read file'));
    reader.readAsText(file);
  });
}